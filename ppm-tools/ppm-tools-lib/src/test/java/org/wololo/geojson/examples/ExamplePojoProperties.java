package org.wololo.geojson.examples;


/**
 * An example POJO properties class containing two attributes.
 */
public class ExamplePojoProperties {
    
    private String str;
    private int idx;

    public String getStr() {
        return this.str;
    }
    public void setStr(String str) {
        this.str = str;
    }

   public int getIdx() {
        return this.idx;
    }
    public void setIdx(int idx) {
        this.idx = idx;
    }
}
